這個模板會將下列圖示變成一個新的模板，每個輸入都會產生不同圖示<br/>
輸入格式如下：
<pre><nowiki>{{Icon/異常狀態|切割|text|black}}</nowiki></pre>
產生{{Icon/異常狀態|切割|text|black}}.

這個範本總共有四個輸入欄位：<br/>
#輸入狀態的全名。
#輸入"text"會產生狀態文字，如果不填寫將不會生成文字
#可以選擇"black"或"white"。
**"black"會產生黑色圖示。
**"white"會產生白色圖示。
**預設會產生黑色圖示，如果沒有特別需要改變顏色可以忽略不輸入。
#輸入<code>imgsize=</code>可以改變圖示的大小，預設寬度為18px。
因此：<br/>
如果你要插入黑色病毒狀態的圖示，輸入<code><nowiki>{{Icon/異常狀態|病毒|text|black}}</nowiki></code>或是<code><nowiki>{{Icon/異常狀態|病毒|text}}</nowiki></code>，會產生{{Icon/異常狀態|病毒|text|black}}。
如果你要插入一個白色的爆炸狀態圖示，且不要包含狀態文字，輸入<code><nowiki>{{Icon/異常狀態|爆炸||white}}</nowiki></code>，會產生{{Icon/異常狀態|爆炸||white}}。
元素和複合元素會產生彩色的圖案，例如：{{Icon/異常狀態|冰凍|text}}{{Icon/異常狀態|電擊|text}}{{Icon/異常狀態|火焰|text|white}}{{Icon/異常狀態|毒素|text|white}}
